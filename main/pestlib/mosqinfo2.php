    <h2 style="color: #68051c; border-bottom: 2px solid #68051c">How to minimise the breeding of mosquitoes in your premises</h2>
    <p style="color: black; font-size: 15px">To get rid of mosquitoes in your home, the best solution is to prevent them from breeding around your home and to prevent them from entering your home. 
</p>
<h4 style="color: black">Avoid being bitten by mosquitoes: -</h4>
   <p style="color: black; font-size: 15px">When outside at dusk or dawn, wear long sleeves, trousers, footwear and hats to reduce exposed skin. <br>
Use mosquito repellent sprays on exposed skin. <br>
Use mosquito repelling products or candles when sitting</p>
<h4 style="color: black">Prevent mosquitoes being able to develop in standing water:</h4>
<p style="color: black; font-size: 15px">Ensure drains are kept clear and running.<br> 
Ensure gutters and water gullies are not blocked. <br>
Cover water butts with a lid. <br>
Do not leave standing water (e.g. in watering cans). <br>
Introduce mosquito fish (Gambusia affinis) or guppies into garden ponds to eat mosquito larvae.
</p>
<h4 style="color: black">Finally, you can help prevent mosquitoes entering your home: </h4>
<p style="color: black; font-size: 15px">After dark, keep windows and doors closed or block out the light with curtains. <br>
Fit mosquito screens to windows.
</p>
