    <h1 style="color: #68051c; border-bottom: 2px solid #68051c">Signs of Termites infestation</h1>
    <p style="color: black; font-size: 15px">Termite infestation is usually not apparent until severe damages have incurred. They live in dark, humid and protected environment, which make them so hard to find - until it’s too late.<br>

As termites devour their way through wood from the inside, it can be rather challenging to detect a termite infestation but there are some tell-tale signs that indicate their presence.<br>

Here are a few common signs of termites that you might see (or hear) around your home or business premises:
</p>
<br>
<div class="card">
    <div class="card-body">
      <p style="font-size: 15px">
        <strong style="color: black">Mud tubes on wall - </strong>
Subterranean termites build shelter tubes made of mud, dirt and debris in order to travel to and fro the food source without being seen. These tubes are about the size of a coin and are usually found on exterior and interior walls leading up to the entry points of the building.    </p>
    <p></p>
    </div>
  </div>
  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
        <strong style="color: black">Sightings of termite swarmers (flying termites) or discarded wings – </strong>
 Usually the first sign of infestation noticed by property owners are the presence of swarmers or alates. Another common indication is the remnants of discarded wings on windowsills and floors. While they may quickly disappear after they found their mating partner, the identical and disembodied wings are sure signs of an indoor termite swarm.    </p>
    <p></p>
  </div>
  </div>
  
  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
        <strong style="color: black">Papery or hollow sounding timber - </strong>
Termites usually consume woods from the inside out, leaving a thin veneer of timber or paint. So when you knock or tap on an area that has termite damages, it will sound hollow or papery due to parts (or all) of the timber having been eaten away.    </p>
    <p></p>
  </div>
  </div>
  
  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
        <strong style="color: black">Tight fitting door or hard to open window - </strong>
As termites devour timber, their excrement or ‘mud’ creates a protective environment that traps heat and moisture. This causes timber to swell, making it harder to open or close the infested windows and doors.    </p>
    <p></p>
  </div>
  </div>

<div class="card">
    <div class="card-body"><p style="font-size: 15px">
        <strong style="color: black">Tunnels in the wood - </strong>
Also known as 'galleries' which are quite difficult to see from the outside.</p>
    <p></p>
  </div>
  </div>


  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
        <strong style="color: black">Termite droppings - </strong>
After consuming wood, dry wood termites often leave behind brown-coloured and grainy faecal mounds. These faecal pellets are usually found beneath the infested wood.
</p>
    <p></p>
  </div>
  </div>


  
  
</p>
