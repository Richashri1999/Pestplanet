    <p style="color: black; font-size: 15px">Cockroaches are a high risk pest to have in your premise. They carry a range of serious illnesses, including salmonella food poisoning, dysentery, diphtheria, gastroenteritis and typhoid. Furthermore, cockroaches have been identified as a major source of powerful allergens, often the cause of asthma, allergies and eczema, especially in urban areas. The health risk is particularly high in homes with children, the elderly or those fighting illnesses. <br/><br/>

Beyond the health risks, cockroaches are a distasteful pest to have in your premise. They are not only a disturbing sight, they cause contamination and can produce an obnoxious odour that taints food and objects which they come in contact with. If the conditions are good, and temperatures sufficient, as is the case all around the year in most parts of India, cockroaches can breed rapidly. They are highly resilient and have developed resistances to a number of insecticides. A quick response is therefore essential to ensure that cockroach infestations do not go out of control or spread to neighbouring properties. A quick response at the very first <mark style="color: red; border-bottom: 1px solid red">sign of an infestation</mark> is essential to control cockroaches in your property and to stop the spread of this pest to your neighbours.
