    <h1 style="color: #68051c; border-bottom: 2px solid #68051c">Signs of cockroach infestation</h1>
    <p style="color: black; font-size: 15px">Cockroaches are most active during the night, preferring to stay hidden in cracks and crevices during the day. They are attracted to food available in your property and will feed on nearly anything from food crumbs to animal droppings / faeces.<br/><br/> 

If you suspect a cockroach infestation, it is essential to take immediate action to ensure a fast solution to the problem and to minimise health risks associated with this pest. 
<br/><br/>
Here are some tell-tale signs that indicate cockroach presence in your premises.
</p>
<br/>




  <div class="card">
    <div class="card-body">
    	<p style="font-size: 15px">
			<strong>Droppings</strong>
			" - black grainy droppings can be found in the cabinets and along the walls or routes that they travelled on."
		</p>
		<p></p>
    </div>
  </div>
  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
			<strong>Smear marks</strong>
			" - where water is abundant, brown and irregular shaped smear marks will appear on horizontal surfaces and wall floor junctions."
		</p>
		<p></p></div>
  </div>
  
  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
			<strong>Moulted skin</strong>
			" - evident of brownish skins can be found close to where they are breeding as immature cockroaches shed skins about 5-8 times when they mature to adult."
		</p>
		<p></p></div>
  </div>
  
  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
			<strong>Egg cases</strong>
			" - appear as small dark brown oval-shaped casings."
		</p>
		<p></p></div>
  </div>

  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
			<strong>Damage</strong>
			" - look for signs of damage on food packaging and organic goods including leather and books."
		</p>
		<p></p></div>
  </div>

  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
			<strong>Unpleasant odour</strong>
			" - a distinct musty smell when cockroaches present in large numbers."
		</p>
		<p></p></div>
  </div>
 
  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
			<strong>Live cockroaches</strong>
			" - usually found around bathrooms and kitchens."
		</p>
		<p></p></div>
