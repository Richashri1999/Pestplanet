    <h2 style="color: #68051c; border-bottom: 2px solid #68051c">DIY monitoring tips</h2>
    <p style="color: black; font-size: 15px">Termites are amazingly proficient when it comes to finding the access to your property. They can get into any building through tiny cracks and crevices less than 1/16 inches wide. And to make matters worse, your property may be sitting on up to six subterranean termite colonies in a single acre plot, with each colony housing up to millions of termites.
<br>
The last thing you want to do is to allow them to infiltrate your home with ease. Here are some effective termite DIY monitoring tips to make sure that your property doesn't become theirs. 
</p>
<br>
<div class="card">
    <div class="card-body">
      <p style="font-size: 15px">
        <strong style="color: black">Remove tree branches, debris, cellulose materials and dead leaves around the property – </strong>
Tree branches or cellulose materials against the wall provide an attractive food source and nesting site for subterranean termites. It can also hide evidence of termite invasion in your home.</p>
    <p></p>
    </div>
  </div>
  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
        <strong style="color: black">Repair leaking taps and pipes - </strong>
 Repairing these leaks will reduce moisture levels, which attract subterranean termites to your property.</p>
    <p></p>
  </div>
  </div>
  
  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
        <strong style="color: black">Use only termite treated wood in garden beds, retaining walls or fence posts - </strong>
Do not use untreated wood to form garden beds or retaining walls, as these will attract termites around your property.</p>
    <p></p>
  </div>
  </div>
  
  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
        <strong style="color: black">Don’t allow climbing plants or bushy gardens to grow against the structure - </strong>
Climbing plants growing against the side of the structure of your property provide termites with entry undetected. The roots of some plants can also penetrate the foundations of your property allowing termites access that is very difficult to locate.</p>
    <p></p>
  </div>
  </div>

<div class="card">
    <div class="card-body"><p style="font-size: 15px">
        <strong style="color: black">Remove dead trees and stumps - </strong>
Dead trees and stumps are ideal sites for termites to nest in. Remove these or have them treated to reduce the risk to your property.</p>
    <p></p>
  </div>
  </div>


  <div class="card">
    <div class="card-body"><p style="font-size: 15px">
        <strong style="color: black">Ensure all scaffolding wood is removed after construction - </strong>
Termites can often attack scaffolding and use it to gain access into your home.
</p>
    <p></p>
